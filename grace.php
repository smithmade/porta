   <p class="question">I don&#8217;t know anyone else named Grace. It feels like someone with that name
would have an old soul, wise beyond their years. Any truth to that?</p>
   <p class="answer">I don’t know if I can say that that’s true for me, but I want it to be.</p>   <div><img src="images/hr_interviews2.png" alt="" width="685px" /></div>
<p class="question">Does Asbury Park have an old soul? The city kind of reminds me of a family.
There&#8217;s the grandfather who served in a war and talks about the past, the
grandmother who collects kitschy beach art, the dad who saw Springsteen
when he was a nobody, the mom who loves buying antiques, and the kids who
think they&#8217;re rebels. And somehow it all comes across as incredibly real and
American. God. Sorry for the tangent.
</p>
   <p class="answer">I think so. It definitely has diehard loyal locals and fans. And Bruce brings a lot of
attention.</p>   <div><img src="images/hr_interviews2.png" alt="" width="685px" /></div>
<p class="question">When I lived in Washington, D.C., there was a lot of talk about how rich whites
were slowly but surely pushing the poor communities ever further East.
Of course, that was the only area of D.C. that I could afford living in. It was
beautiful and scary all at once. Do you think the opposite is happening in
Asbury? The poor being pushed further and further West?
</p>
   <p class="answer">It has seemed that way for a long time. I think it is happening and I think it
is unfortunate. I saw it in every part of Brooklyn that I lived in, also because they
were the only parts that I could afford to live in. It seems accepted as something
that just happens and I don't know if there is a way to avoid it or not, but it seems
that there have to be ways to include the people being pushed out of communities
in the same projects that are changing the communities so that those people can be
involved and develop with it. Maybe instead of more condos, this town needs more
parks. Maybe we should focus on being a great place for a lot of people to come to
for as long as we stand rather than a mediocre place for a few people to live until
interest is lost. I hope I’m not making enemies right now. I just think that everybody
should consider the strong possibility that the trend in development, as it has been
for a very long time, should be reversed.
</p>   <div><img src="images/hr_interviews2.png" alt="" width="685px" /></div>
<p class="question">Do you think Porta has a role in bringing communities back together?</p>
   <p class="answer">Yeah, I do. The idea begins at our tables, I think, and is carried through every idea
that we have building out from the restaurant itself. Community and sharing,
working together for a common goal that benefits everyone.
</p>
<div><img src="images/hr_interviews2.png" alt="" width="685px" /></div>
   <p class="question">I feel like a U.N. Peace Talk could happen at Porta. Can honestly prepared food
stop war?
</p>
   <p class="answer">Absolutely! Honestly prepared food would mean better nutrition, which would
promote better mental health and clarity of thought, which would naturally inhibit
the bad decision-making that promotes war. Easy.
</p>   <div><img src="images/hr_interviews2.png" alt="" width="685px" /></div>
<p class="question">What do you feel your purpose is as a chef? You can steal the idea of stopping
war if you want. I won't mind.
</p>
   <p class="answer">Stopping war goes without saying, and I&#8217;m doing everything I can. My life as it is
right now seems to have kind of happened to me through a series of accidents (for
which I’m grateful) and spontaneous decisions. I did not plan to end up where I
am now. I planned to do a lot of completely unrelated things. I tried out cooking
because I needed a job and I already liked to do it, I ended up loving it and stuck
with it. Whenever I was promoted or offered a new position I went with it, largely
out of curiosity. Though my interests from before I worked in restaurants remain
active interests, I am very grateful that my plans for myself were interrupted and
that I ended up where I did. I guess, as a chef, I try to have fun and encourage others
to do the same. I know a chef who used to tell me when I would get frustrated and
stressed that &#8220;its only food and wine.&#8221; I think about that a lot. Food is about nature's
beauty and the beauty of what can be done with it. It should not be a source
of stress.
</p>   <div><img src="images/hr_interviews2.png" alt="" width="685px" /></div>
<p class="question">I think we should send the people who created Angry Birds a Porta care
package. What should we put in it? (Note to self: create game called Angry
Pigs.)</p>
   <p class="answer">A bird roasted in one of Porta's beautiful ovens. Stuffed with lemons and rosemary.</p>   <div><img src="images/hr_interviews2.png" alt="" width="685px" /></div>
<p class="question">Let&#8217;s keep talking about Angry Birds. Have you played it? It's very medieval.</p>
<p class="answer">
I have not played it. I just know they seem like assholes. Maybe we should send
them some honestly prepared seeds instead. It may brighten their mood.</p>
<div><img src="images/hr_interviews2.png" alt="" width="685px" /></div>
   <p class="question">I think that's the first time I used the word medieval since 6th grade. I want to
keep using it for the remainder of this interview. Can you help me brush up on
my Medieval Literature? All I can think of are the Canterbury Tales.</p>
<p class="answer">
I could definitely help you brush up. I have to warn you, though, that it does have the
tendency to put those weak of heart to sleep. You don’t seem like that type though.
I haven’t read any medieval literature in awhile, but still love it. If you are into
medieval monastic scribal practice and biblical exegesis, I’m your girl.
</p>   <div><img src="images/hr_interviews2.png" alt="" width="685px" /></div>
<p class="question">Do you know that Weird Al Yankovic song where he says he's going to
get &#8220;medieval on your heinie?&#8221; That was<br /> my jam.
</p>
   <p class="answer">Yeah. Amish Paradise?</p>   <div><img src="images/hr_interviews2.png" alt="" width="685px" /></div>
<p class="question">Yeah. Any further thoughts on Weird Al? Shall we discuss his song, &#8220;Eat It&#8221;?
That would probably be more relevant. Or how about &#8220;I Love Rocky Road&#8221;?
Believe it or not, those two songs introduced me to Michael Jackson and Joan
Jett, respectively. So backwards.
</p>
   <p class="answer">I love &#8220;Eat It.&#8221; Porta discourages picky eating. I don’t think I know &#8220;I Love Rocky
Road,&#8221; but I can imagine what that’s like. I’ll look it up.
</p>   <div><img src="images/hr_interviews2.png" alt="" width="685px" /></div>
<p class="question">Cool. Do you sing any Joan Jett in the kitchen? Because that would be
AMAZING. And you kind of come across as a toned-down Joan Jett. Like, really
toned down. But in a cool way.
</p>
   <p class="answer">We sing a lot in Porta&#8217;s kitchen. Paul usually starts the trend and we all follow
because he has a way of getting obnoxious music hopelessly stuck in our heads. His
repertoire seems to lean towards showtunes in general. Sometimes he throws
in some Midler, usually with lyrics that are bent to absolute self-absorption. &#8220;Thank
me, thank me, thank god for me. The wind beneath my wings.&#8221; That sort of thing.
</p>   <div><img src="images/hr_interviews2.png" alt="" width="685px" /></div>
<p class="question">Other than singing, what makes a good kitchen? Having externed at
Millennium in San Francisco, I feel like you need more than music to make a
kitchen accessible and inviting. You need tight communication. And no egos.
You don&#8217;t strike me like the type of person with a big ego.
</p>
   <p class="answer">I don’t think I have a big ego. I’m great and I know it. It’s straight fact, not ego. I’m
definitely the best singer in the kitchen. Fact. In all seriousness, though, I think a
big ego, like excessive and unnecessary stress, is another thing that has no business
with food. Somehow, both of those things are very commonly paired with food. I
think a large part of having a strong kitchen is respect for one another and working
together on a level of equality, rather than oppressive authority. People need to
feel that they are part of something, be given a level of responsibility and have a
sense of creative input. One thing that I love about my position is being able to
notice what other people are doing and consider that another person&#8217;s instinctive
approach to something is better than my own. Sometimes a little detail that I would
not have thought of changes my own ideas or inspires a new chain of ideas. I love
that. Not having gone to culinary school, and not having planned this position for
myself, I think that a major reason why I ended up here is that I had been given
responsibility, was encouraged to be creative and was listened to in the past. Say no
to ego.
</p>   <div><img src="images/hr_interviews2.png" alt="" width="685px" /></div>
<p class="question">Did you go medieval on your ego? Is that your secret?</p>
   <p class="answer">I don’t think it ever really had a chance to over-inflate. If it started to, I probably
went medieval on it. And if anyone disagrees with me, please feel free to go
medieval on my ego.
</p>
   <div><img src="images/hr_interviews2.png" alt="" width="685px" /></div>

   <p class="question">Now I&#8217;m thinking about serfs and farmers. What&#8217;s your vision for Porta and
farming its own produce?
</p>
   <p class="answer">My vision for growing our own produce is enormous and small. I imagine very
healthy and productive gardens in a lot of places, inspiring more healthy and
productive gardens, educating people on how to create them and helping them to be
created. It’s enormous in space, in concept and in inspiration. It is small in that it is
not exclusive. It would encourage and invite everyone interested to get involved, in
our projects or their own, and to form a bond on a personal level. I guess my vision
of this is really that the concept of caring about the connection between people
and their food and really knowing where it comes from would be contagious when
introduced to this area. It seems to me that there would be nothing but benefit for
everyone and everything involved if this were the case.
</p>   <div><img src="images/hr_interviews2.png" alt="" width="685px" /></div>
<p class="question">Do you think the whole farm-to-table thing is just a marketing ploy? It seems
like everyone and their mother is doing<br />it nowadays.
</p>
   <p class="answer">I think it is often violated as a marketing ploy. I think it’s also not something that
seems possible to a lot of people. Sometimes it just isn’t what people want. However,
a tomato delivered in a box from a warehouse that also distributes meat, seafood,
plastic cups and pre-packaged desserts (to name a few) cannot be compared to a
just-picked tomato from a farmer&#8217;s market or a person's backyard garden. The life
and beauty of fresh food fades very dramatically when it travels as far and sits in a
warehouse as long as it often does to get to us. The farm-to-table concept is not an
easy one to work with and is not always something that can be done exclusively,
but there are a lot of measures that can be taken by us to make it easier to practice.
Ideally, everyone and their mother would be doing it and there would be no
marketing ploy. It would be the standard approach.
</p>   <div><img src="images/hr_interviews2.png" alt="" width="685px" /></div>
<p class="question">Can farmers&#8217; markets compete with the likes of Whole Foods and other
big name supermarkets? I feel like there&#8217;s<br />a huge resurgence in American
farming, at least on an independent level. Are people looking for an
alternative to<br />corporate agribusiness?
</p>
   <p class="answer">I think so. And I think that the interest is there. It probably seems more expensive
to people than it actually is. Growing food is not expensive if done resourcefully.
People generally seem to think that farmers’ markets are the better way to go when
the option is there. I used to shop at the Union Square farmer's market in New
York a couple of times a week when I worked in the city and it was always packed.
Sometimes everybody was just at a standstill. You couldn’t even walk because it was
so crowded. Union Square is directly across 14th Street from a huge Whole Foods.
It seemed like a good sign to me. I have never seen a supermarket as crowded as
that farmer&#8217;s market was, which is easily the size of a supermarket and doesn’t even
offer half of the stuff that a supermarket does.
</p>   <div><img src="images/hr_interviews2.png" alt="" width="685px" /></div>
<p class="question">How much of Porta&#8217;s produce comes from big box farms?
</p>
   <p class="answer">We use a produce company that deals with a lot of smaller local farms, so a lot of
our produce comes from small farms through them. They get the stuff for us as
specialty items, not things that they usually have in stock. Of course, when farm
stands around here are closed for the winter, some of what we use comes from
ever fertile and paradisiacal California. We are hoping to produce a large part of the
produce that we use this summer. It’s a very ambitious goal, but doesn’t seem out of&nbsp;reach.

</p>   <div><img src="images/hr_interviews2.png" alt="" width="685px" /></div>
<p class="question">Is the whole idea of &#8220;big&#8221; on the way out?
</p>
   <p class="answer">Yes. In a lot of ways.</p>   <div><img src="images/hr_interviews2.png" alt="" width="685px" /></div>
<p class="question">How about big butts?
</p>
   <p class="answer">They seem to be an exception.</p>   <div><img src="images/hr_interviews2.png" alt="" width="685px" /></div>
<p class="question">Should we go medieval on them?
</p>
   <p class="answer">I dont think so. I think we should let them live. They’re not hurting anybody.</p>   
