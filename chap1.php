  <div id="vol5_meat">
  Prometheus is the Titan who in Greek Mythology, flew to the sun in order to give Man
the one thing that he was forbidden; fire. When the attributes of the animals were
  <img src="images/essay5/containing.png" alt="Containing" class="containing" />
dished out, Man was left naked and forgotten,
without feathers or claws, without scales or fur.
Prometheus, out of his love for Man – the
bare pale animal, delivered fire so that Man
may endure. Fire may be the origin of
civilization and its progress. For his theft,
Prometheus was punished by Zeus and
condemned to eternal torment. Prometheus
was bound to a rock where each day an eagle
would tear out his liver with talons and beak.
His liver would grow back each day to be torn out the next. Prometheus was
understood to have a profound intelligence and power of forethought; he is punished
for the conscious decision that he made to act against the gods, a decision that
  <img src="images/essay5/torch.png" alt="Torch" class="torch" />
produced an infinite future for humanity.
Containing fire has given us power. At
times we feel the depth of what fire is
capable of giving, we feel an ancient
connection, and a force as if there were
nothing else, as it may have been at one
time – just man and fire.
