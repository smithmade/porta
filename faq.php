  <p class="answer">The park (PNP as we like to call it) is Porta&#8217;s outdoor seating/gaming area and a sub-brand of PORTA. It is most active during the late spring, summer and fall, when we&#8217;re able to open up PORTA&#8217;s garage doors and serve drinks outside. In the winter we use the park to sell Christmas trees felled in the forests <br />of Oregon.</p>
