<p class="question">Summertime at Porta is like the Eighth Wonder of the World. Can you give us a picture of what it’s like to work in the kitchen on a busy summer day?</p>

   <p class="answer">It gets pretty crazy. There are so many different things happening all at once, 
    I don’t know where to start: you’ve got the sound of the constant ticket machine printing order after 
    order and once you think it’s done, the second wave comes and it wipes out all the prep you thought would have 
    lasted you through the night. So you try and find a free hand to help while there are pots and pans clanking, 
    while you’re trying to listen to the order you should be firing next, a server coming to correct a ticket that 
    they put in wrong while yelling for a food runner to run all the food that’s accumulating in the window. For some 
    people this may seem like a hectic situation, but for me all the noise becomes a melody that you try and keep up 
    with through the dinner service that feels like it’s never going to end.</p>   

<div><img src="images/hr_interviews2.png" alt="" width="685px" /></div>

<p class="question">Do you feed off of that kind of energy?</p>

   <p class="answer">Of course. That's the kind of energy that I feel gets all of us through the summer. 
    We’re all like a family trying to help each other in any way and serve people great food.</p>   

<div><img src="images/hr_interviews2.png" alt="" width="685px" /></div>

