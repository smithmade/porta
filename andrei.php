<p class="question">We heard you used to build boats back home. That sounds awesome. How’d you learn how to do that?</p>

   <p class="answer">Well my grandfather and my father used to work in a shipyard for so long, and like every son, I wanted to 
    follow in his steps, so, I decided to try it. But I gotta say I started building my first boat in New Jersey with my friends.</p>   

<div><img src="images/hr_interviews2.png" alt="" width="685px" /></div>

<p class="question">And how’d you end up in New Jersey?</p>

   <p class="answer">My friend who I had been working with in Key West, he is from NJ. When he moved back here from 
    Florida, he asked me if I wanted come over and work with him in the kitchen again, and that’s how I ended up in New Jersey.</p>   

<div><img src="images/hr_interviews2.png" alt="" width="685px" /></div>

<p class="question">When did you figure out that you liked making pizza?</p>

   <p class="answer">I love cooking, and pizza is one of those things that I like to eat but I never knew how to make it,&nbsp;so I 
    figured I should try to learn how to make a pizza, and it wasn't that easy. It takes a while to&nbsp;learn, and every time you 
    are learning new tricks, probably only after 8 months I started feeling that&nbsp;I'm close to basic knowledge about making pizza, 
    so honestly it takes a lot of practice and passion to learn.</p>

<div><img src="images/hr_interviews2.png" alt="" width="685px" /></div>

   <p class="question">Tell us a little bit about what it’s like working the ovens on a busy night.
</p>
   <p class="answer">It’s not that hard. All you gotta do is focus on what you are doing, and just keep making pizza. 
    But most important is who you are working with. If your team is great, that means work will be done no matter what 
    and how busy it is.
</p>   

