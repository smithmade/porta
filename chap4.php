	<p class="p_fix_chap4">You can sort of measure the heat of the oven when you put a fresh log in both by how
quickly it lights up, and then how far the flame curls around the top of the inner dome.
If you get black smoke or a circle of flame, it’s hotter than hell, try and keep the floor
temp down.</p>
<p><strong>Otherwise, cook at as high a temperature as possible. This is just
the way that turns out to be the most fun.</strong></p>
<p>When a pizza is sticky it helps to almost drop the peel on the oven floor and pull it out
quickly. The air coming up through the bottom of the peel helps give a little force to
get the sticky dough off without catastrophe – one smooth movement.</p>
<p><strong>It helps to divide the sections of the oven floor into different zones
that you work within.</strong> I always kept the back raging hot incase my floor cooled
off, you can drop a pie for a 5 count and have a nicely spotted bottom.</p>
<img src="images/essay5/sweet_spot.png" alt="Sweet Spot" class="sweet_spot" />
<p><span class="big_red">There’s a sweet
spot on the oven,</span>
especially when you’ve been
cooking for a while, usually right
inside and to the left, the farthest
spot from the fire, where if you
drop a pizza you won’t have to
touch it, <strong>just time it right
and take out a perfect
cook without a spin.</strong></p>
<p class="egg_pies">With the egg pies its always good to keep them closer to the window so you can keep
the pizza in the oven without burning every bit of crust before the egg is cooked.
<strong>This is connected to dividing the sections of the oven into zones.</strong></p>
<p><strong>Always keep the pizzas in motion;</strong> if the pie is touching your peel it should
never stop moving.</p>
<p><strong>The force of the fire is really quite incredible,</strong> so to aim the lightest parts
of the crust directly at the flame is the best way to maintain control over a consistent
crust.</p>
<p>If the bottom of the pie is finished but the crust or the top isn’t, hold the pie up off
the ground with the peel, the higher the hotter.</p>
<p><strong>It’s important to clean the oven with care.</strong> This is like brushing a horse or
something. Maintaining the object attaches you to it in some way, to treat it with care
has an effect on the way you cook.</p>
<p><strong>It’s important to sort of
communicate with the oven.</strong> It
seems to tell you what it needs
 if you pay
attention to how it changes. It’s a
breathing organism.</p>
<img src="images/essay5/stretchers.png" alt="Stretchers" class="stretchers" /><p><strong>Pet the beast on its head,</strong> where
the smoke rises from the window and
goes up into its skull, this is one reason it
resembles a blind beast, it has <br>no eyes.</p>
