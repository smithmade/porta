   <p class="answer">Some have the stickers on the brim, some don’t, never the tags though. Never really got into that. </p>   

<div><img src="images/hr_interviews2.png" alt="" width="685px" /></div>

<p class="question">Does anyone still wear their hats sideways? God I hope not.</p>

   <p class="answer">Unfortunately yes, usually lost souls who still believe they live in the 90s.</p>   

<div><img src="images/hr_interviews2.png" alt="" width="685px" /></div>

<p class="question">You used to work in the city at Maialino. For you, what are the differences between the NYC food scene and the one (or lack of one) down here?</p>

   <p class="answer">The New York food scene is cool and all, very competitive, a lot of egos between the big time chefs, 
    but I was always more drawn to the camaraderie and brotherhood between the cooks in the kitchen. We would go out and 
    be treated like celebrities at a bunch of restaurants and bars because there was just that understanding of what we do 
    on a daily basis. You don’t really see that down here, everyone’s just really worried about how much money they can make 
    and how cool they look doing it. I think Porta is trying to change that and bring a real food scene back to New Jersey.</p>

<div><img src="images/hr_interviews2.png" alt="" width="685px" /></div>

   <p class="question">When you’re not working, what’s your favorite thing to cook for yourself?</p>
   <p class="answer">Believe it or not, I really only cook Asian food at home. I love the flavor combinations. If you look 
    in my cabinets you’ll only find sesame oil, rice wine vinegar, some form of curry and sriracha. Every once in a while I’ll 
    make something Italian per request.</p>   

<div><img src="images/hr_interviews2.png" alt="" width="685px" /></div>

<p class="question">It seems to me that most people don’t know how to cook anymore. Like it’s a lost art that is slowly disappearing with each new generation. Do you find any truth in that or am I completely alone on this one?</p>

   <p class="answer">You’re absolutely right. It’s almost like a de-evolution is happening, I think it’s because of how 
    readily available and convenient grab and go meals are now a days. Unless you’re a chef, or do this for a living, you 
    have no real primal need/instinct to know how to cook, now a days someone or something will do it for you. </p>   

<div><img src="images/hr_interviews2.png" alt="" width="685px" /></div>

<p class="question">50 years from now, what will Porta be remembered for?</p>

   <p class="answer">Porta will be remembered for being an anomaly. The first of its kind. A place that took all the best 
    concepts and ideas of a restaurant/bar and packed it into one giant space, and never looked back.</p>   




